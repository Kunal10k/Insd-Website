    stats.forEach(item => {
        item.innerText = "0";

        const animate = () => {
            const target = +item.getAttribute("data-num");
            const current = +item.innerText;
            const speed = target / 120;

            if (current < target) {
                item.innerText = Math.ceil(current + speed);
                setTimeout(animate, 25);
            } else {
                item.innerText = target;
            }
        };

        animate();
    });
